import React from "react";
import {
  Article,
  Title,
  Lead,
  H2,
  H3,
  P,
  Ul,
  Formula,
  Callout,
  Term,
  Sources,
} from "../components/ArticleKit";
import { REFS } from "../references";

const LossArticle: React.FC = () => (
  <Article>
    <Title>Функции потерь</Title>
    <Lead>
      <Term id="loss">Функция потерь</Term> измеряет, насколько предсказание сети
      далеко от правильного ответа. Обучение — это минимизация её среднего
      значения по данным.
    </Lead>

    <H2>Для регрессии</H2>
    <H3>MSE — среднеквадратичная ошибка</H3>
    <P>Штрафует за квадрат отклонения, сильно реагирует на выбросы.</P>
    <Formula>MSE = (1/n) · Σ (yᵢ − ŷᵢ)²</Formula>

    <H3>MAE — средняя абсолютная ошибка</H3>
    <P>Штрафует линейно, устойчивее к выбросам, чем MSE.</P>
    <Formula>MAE = (1/n) · Σ |yᵢ − ŷᵢ|</Formula>

    <H2>Для классификации</H2>
    <H3>Кросс-энтропия</H3>
    <P>
      Сравнивает предсказанное распределение вероятностей с истинным. Для
      многоклассовой задачи используется вместе с{" "}
      <Term id="softmax">softmax</Term>.
    </P>
    <Formula>L = − Σ yᵢ · log(ŷᵢ)</Formula>

    <Callout variant="tip">
      Соответствие «задача → потеря»: регрессия → MSE/MAE; бинарная
      классификация → binary crossentropy; много классов → categorical
      crossentropy.
    </Callout>

    <Callout variant="info">
      Значение потери само по себе не интерпретируемо — важна динамика: на
      обучении и валидации она должна снижаться. Если на валидации растёт —
      это <Term id="overfitting">переобучение</Term>.
    </Callout>

    <H3>Бинарная кросс-энтропия</H3>
    <P>
      Частный случай для двух классов, работает в паре с{" "}
      <Term id="sigmoid">sigmoid</Term>:
    </P>
    <Formula>L = −[ y·log(ŷ) + (1−y)·log(1−ŷ) ]</Formula>

    <H3>Huber loss</H3>
    <P>
      Компромисс между MSE и MAE: ведёт себя как квадратичная вблизи нуля и как
      линейная при больших отклонениях — устойчива к выбросам и при этом гладкая.
    </P>

    <H2>Почему нельзя MSE для классификации</H2>
    <P>
      С sigmoid/softmax квадратичная ошибка даёт почти нулевой градиент при
      сильно ошибочных уверенных предсказаниях — обучение «застревает».
      Кросс-энтропия лишена этого недостатка и сходится быстрее.
    </P>

    <H2>Потери и метрики — это разное</H2>
    <Ul
      items={[
        "Функция потерь должна быть дифференцируемой — по ней считают градиент.",
        "Метрика (accuracy, F1, ROC-AUC) измеряет качество для человека и может быть недифференцируемой.",
        "Оптимизируют потерю, а отчитываются по метрике.",
      ]}
    />

    <Sources refs={[REFS.goodfellow, REFS.nikolenko]} />
  </Article>
);

